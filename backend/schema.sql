-- Main grants table
CREATE TABLE IF NOT EXISTS grants (
  id SERIAL PRIMARY KEY,
  name TEXT NOT NULL,
  description TEXT,
  source TEXT NOT NULL,                 -- 'EU Portal', 'Balc√£o Fundos', 'FCT', 'EUFunding'
  source_url TEXT NOT NULL,
  amount TEXT,
  deadline DATE,
  relevance INTEGER DEFAULT 0,          -- 0-100 AI match score
  status TEXT DEFAULT 'active',         -- 'active', 'closing_soon', 'closed'
  region TEXT,                          -- 'EU', 'Portugal', 'Other'
  country TEXT,
  eligibility TEXT,
  contact TEXT,
  category TEXT,                        -- 'AI', 'Web3', 'OEPUGenAI'
  raw_data JSONB,
  days_left INTEGER,
  created_at TIMESTAMP DEFAULT NOW(),
  updated_at TIMESTAMP DEFAULT NOW()
);

-- Deep research data (Gemini 2.5 Pro + AI-Researcher)
CREATE TABLE IF NOT EXISTS research_data (
  id SERIAL PRIMARY KEY,
  grant_id INTEGER REFERENCES grants(id) ON DELETE CASCADE,
  business_model TEXT,                  -- Generated by Gemini
  business_canvas_url TEXT,             -- Generated canvas image
  success_factors JSONB,                -- Critical success factors
  evaluation_criteria JSONB,            -- How grant is evaluated
  past_winners JSONB,                   -- Analysis of past winners
  deep_research JSONB,                  -- Full Gemini deep research output
  created_at TIMESTAMP DEFAULT NOW()
);

-- Generated proposals (DUAL MODE)
CREATE TABLE IF NOT EXISTS proposals (
  id SERIAL PRIMARY KEY,
  grant_id INTEGER REFERENCES grants(id) ON DELETE CASCADE,
  mode TEXT NOT NULL,                   -- 'fast' or 'research' (CRITICAL!)
  executive_summary TEXT,
  full_proposal TEXT,                   -- Markdown format
  budget JSONB,
  timeline JSONB,
  status TEXT DEFAULT 'draft',
  gemini_model TEXT DEFAULT 'gemini-2.0-flash-exp',
  generation_time INTEGER,              -- seconds
  enhanced_features JSONB,              -- Which of 43 features applied
  created_at TIMESTAMP DEFAULT NOW()
);

-- Multi-agent thinktank sessions
CREATE TABLE IF NOT EXISTS thinktank_sessions (
  id SERIAL PRIMARY KEY,
  grant_id INTEGER REFERENCES grants(id) ON DELETE CASCADE,
  researchers JSONB,                    -- 5 AI agents
  messages JSONB,                       -- Full conversation
  status TEXT DEFAULT 'active',
  final_output TEXT,
  created_at TIMESTAMP DEFAULT NOW(),
  completed_at TIMESTAMP
);

-- Stripe Customers (Standard Supabase SaaS Schema)
CREATE TABLE IF NOT EXISTS customers (
  id UUID REFERENCES auth.users NOT NULL PRIMARY KEY,
  stripe_customer_id TEXT
);

-- Stripe Subscriptions (Standard Supabase SaaS Schema)
CREATE TABLE IF NOT EXISTS subscriptions (
  id TEXT PRIMARY KEY,
  user_id UUID REFERENCES auth.users NOT NULL,
  status TEXT,
  metadata JSONB,
  price_id TEXT,
  quantity INTEGER,
  cancel_at_period_end BOOLEAN,
  created TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  current_period_start TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  current_period_end TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  ended_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  cancel_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  canceled_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  trial_start TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  trial_end TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- Indexes
CREATE INDEX IF NOT EXISTS idx_grants_status ON grants(status);
CREATE INDEX IF NOT EXISTS idx_grants_deadline ON grants(deadline);
CREATE INDEX IF NOT EXISTS idx_grants_category ON grants(category);
CREATE INDEX IF NOT EXISTS idx_research_grant_id ON research_data(grant_id);
CREATE INDEX IF NOT EXISTS idx_proposals_grant_id ON proposals(grant_id);

-- Auto-update trigger
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
   NEW.updated_at = NOW();
   RETURN NEW;
END;
$$ language 'plpgsql';

DROP TRIGGER IF EXISTS update_grants_updated_at ON grants;
CREATE TRIGGER update_grants_updated_at BEFORE UPDATE
ON grants FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

