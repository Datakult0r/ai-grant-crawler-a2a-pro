CREATE TABLE IF NOT EXISTS grant_sources (
    id SERIAL PRIMARY KEY,
    url TEXT NOT NULL UNIQUE,
    name TEXT,
    type TEXT, -- 'portal', 'aggregator', 'direct'
    last_crawled_at TIMESTAMP,
    status TEXT DEFAULT 'active'
);

