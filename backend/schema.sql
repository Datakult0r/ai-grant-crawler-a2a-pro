-- RLS Policies
-- Using DO blocks to check existence before creating

-- Grants
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_policies WHERE tablename = 'grants' AND policyname = 'Public grants are viewable by everyone'
    ) THEN
        CREATE POLICY "Public grants are viewable by everyone" ON grants
        FOR SELECT USING (true);
    END IF;

    -- Allow writes for authenticated users (or service role which bypasses RLS)
    -- For now, we allow public insert/update to ensure backend works if using anon key
    -- In production, this should be restricted to service role or admin users
    IF NOT EXISTS (
        SELECT 1 FROM pg_policies WHERE tablename = 'grants' AND policyname = 'Enable insert for everyone'
    ) THEN
        CREATE POLICY "Enable insert for everyone" ON grants FOR INSERT WITH CHECK (true);
    END IF;

    IF NOT EXISTS (
        SELECT 1 FROM pg_policies WHERE tablename = 'grants' AND policyname = 'Enable update for everyone'
    ) THEN
        CREATE POLICY "Enable update for everyone" ON grants FOR UPDATE USING (true);
    END IF;
END
$$;

